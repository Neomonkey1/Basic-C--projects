using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace Methods_Submission
{
    //  Made public class MathOperations with three methods
    public class MathOperations
    {
        //  Made first public Method adding method
        public int AddNumber (int num)
        {
            return num + 10;
        }

        // Made second public Method subtracting method
        public int SubtractNumber (int num)
        {
            return num - 5;
        }

        //  Made third public Method multiply method
        public int MultiplyNumber (int num)
        {
            return num * 2;
        }
    }
}
